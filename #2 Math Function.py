import math

pi = 3.14

print(pi)               # origin 
print(round(pi))        # 3
print(math.ceil(pi))    # 4
print(math.floor(pi))   # 3

print(abs(-pi))         #+3.14

print(3.14*3.14)
print(pow(3.14,2))      # pow(value,開多少次方)

print(math.sqrt(3.14*3.14))    # 開根號

x = 1
y = 2
z = 3
print(max(x,y,z))       # show the max value
print(min(x,y,z))       # show the min value
